export function DbIcon({
  driver,
  className,
  active,
  color,
}: {
  driver: string;
  className?: string;
  active?: boolean;
  color?: string | null;
}) {
  const baseClass = cn(
    "h-3.5 w-3.5 shrink-0 transition-colors",
    active ? "text-foreground" : "text-muted-foreground/50",
    className,
  );
  const styleColor =
    active && color ? ({ color } as React.CSSProperties) : undefined;

  if (driver === "postgres") {
    return (
      <svg
        role="img"
        viewBox="0 0 118.53 122.88"
        xmlns="http://www.w3.org/2000/svg"
        className={baseClass}
        style={styleColor}
      >
        {POSTGRES_PATHS.map((d, i) => (
          <path key={i} d={d} fill="currentColor" />
        ))}
      </svg>
    );
  }

  if (driver === "mariadb") {
    return (
      <svg
        role="img"
        viewBox="0 0 24 24"
        xmlns="http://www.w3.org/2000/svg"
        className={baseClass}
        style={styleColor}
      >
        <path d={MARIADB_PATH} fill="currentColor" />
      </svg>
    );
  }

  if (driver === "mysql") {
    return (
      <svg
        role="img"
        viewBox="0 0 128 128"
        xmlns="http://www.w3.org/2000/svg"
        className={baseClass}
        style={styleColor}
      >
        <path d={MYSQL_PATH} fill="currentColor" />
      </svg>
    );
  }

  // fallback: sqlite (ou qualquer outro driver desconhecido com ícone
  // genérico do sqlite).
  return (
    <svg
      role="img"
      viewBox="0 0 24 24"
      xmlns="http://www.w3.org/2000/svg"
      className={baseClass}
      style={styleColor}
    >
      <path d={SQLITE_PATH} fill="currentColor" />
    </svg>
  );
}
